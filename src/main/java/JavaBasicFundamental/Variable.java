package JavaBasicFundamental;

public class Variable {
    public static void main(String[] args) {
        int a = 1;
        double d = 6.9;
        String name = "Raji";
        boolean b = false;
        char c = 'A';
        System.out.println(a);
        System.out.println(d);
        System.out.println(name);
        System.out.println(b);
        System.out.println(c);
    }
}
