package JavaBasicFundamental;

public class TypecastingAndTypeConversions {
    public static void main(String[] args) {
        int a = 100;
        byte b = (byte)a;
        System.out.println(b);
        float f = 1.6F;
        int c = (int)f;
        System.out.println(c);
        double d = (double)f;
        System.out.println(d);
    }
}
